// 5. Números romanos
let numRomano = parseInt(prompt("Ingrese un número del 1 al 10:"))

switch (numRomano) {
    case 1: alert("I"); break;
    case 2: alert("II"); break;
    case 3: alert("III"); break;
    case 4: alert("IV"); break;
    case 5: alert("V"); break;
    case 6: alert("VI"); break;
    case 7: alert("VII"); break;
    case 8: alert("VIII"); break;
    case 9: alert("IX"); break;
    case 10: alert("X"); break;
    default: alert("Número inválido")
}
